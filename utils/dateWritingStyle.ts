const PRESET_ALIASES: Record<string, string> = {
    natural: 'cozy',
    daily: 'cozy',
    literary: 'immersive',
    poetic: 'cinematic',
};

export function resolveDateWritingStylePreset(key: string): DateWritingStylePreset | null {
    const resolved = PRESET_ALIASES[key] || key;
    return DATE_WRITING_STYLE_PRESETS.find(p => p.key === resolved) || null;
}

/** 返回注入到 system prompt 末尾的文风段落 */
export function buildDateWritingStylePrompt(writingStyle?: string): string {
    const parts: string[] = [];
    parts.push(`\n${ANTI_CLICHE_BASE}`);
    if (writingStyle && writingStyle.trim()) {
        const preset = resolveDateWritingStylePreset(writingStyle.trim());
        if (preset) {
            parts.push(`\n${preset.prompt}`);
        } else {
            parts.push(`\n【文风指令：自定义】\n${writingStyle.trim()}`);
        }
    }
    return parts.join('\n');
}
